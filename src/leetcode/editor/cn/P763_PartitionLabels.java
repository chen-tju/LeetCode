class Solution {
    public List<Integer> partitionLabels(String S) {
        //å®šä¹‰ä¸€ä¸ªé•¿åº¦ä¸º26çš„æ•°ç»„ï¼Œç”¨äºå­˜æ”¾æ¯ä¸ªå­—æ¯æœ€åä¸€æ¬¡å‡ºç°çš„ä¸‹æ ‡
        int[] last = new int[26];
        for(int i = 0; i < S.length(); i++){
            last[S.charAt(i) - 'a'] = i;
        }
        List<Integer> res = new ArrayList<>();
        int start = 0, end = 0;
        for(int i = 0; i < S.length(); i++){
            end = Math.max(end, last[S.charAt(i) - 'a']);
            if(i == end){
                res.add(end - start + 1);
                start = end + 1;
            }
        }
        return res;
    }
}
